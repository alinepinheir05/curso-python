# Prompt the user to enter a word
# and assign it to the user_word variable.
user_word = input('Insira uma palavra:')
user_word = user_word.upper()
# letter = A, E, I, O, U
for caractere in user_word:
    if caractere == 'A':
        continue
    elif caractere == 'E':
        continue
    elif caractere == 'I':
        continue
    elif caractere == 'O':
        continue
    elif caractere == 'U':
        continue
    else:
        print(caractere)
